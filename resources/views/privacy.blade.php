                <h2>Persoonsgegevens die we verzamelen</h2>

                <h3>
                    Wat zijn persoonsgegevens?
                </h3>
                <p>
                    Persoonsgegevens zijn gegevens over een geïdentificeerde of identificeerbare natuurlijke
                    persoon. Een identificeerbare natuurlijke persoon is iemand die rechtstreeks of indirect kan
                    worden geïdentificeerd aan de hand van een identificator zoals een naam, een
                    identificatienummer, locatiegegevens, een online identificator of een of meer elementen die
                    kenmerkend zijn voor de fysieke, fysiologische, genetische, psychische, economische, culturele
                    of sociale identiteit van die natuurlijke persoon.
                </p>

                <p>
                    Gegevens die geanonimiseerd of geaggregeerd zijn, zodat deze niet meer kunnen worden gebruikt om
                    een specifieke natuurlijke persoon te identificeren, hetzij in combinatie met andere informatie
                    hetzij anderszins, beschouwen we niet als persoonsgegevens.
                </p>
                <p>
                    We verzamelen persoonsgegevens van u wanneer u onze Diensten gebruikt.
                </p>
                <p>
                    We verzamelen persoonsgegevens van u en de apparaten (inclusief mobiele apparaten) die u
                    gebruikt wanneer u: onze Diensten gebruikt, zich inschrijft voor een account bij ons, ons
                    informatie verstrekt via een webformulier, uw account bijwerkt of er gegevens aan toevoegt,
                    deelneemt aan discussies in chats of aan of de oplossing van geschillen of wanneer u op andere
                    wijze met ons communiceert.
                </p>
                <p>

                    Sommige persoonsgegevens, zoals gegevens die u identificeren, zijn noodzakelijk om akkoord te
                    gaan met onze Gebruiksvoorwaarden. De verstrekking van alle overige persoonsgegevens is
                    vrijwillig, maar kan vereist zijn om gebruik te kunnen maken van onze Diensten, zoals informatie
                    over uw advertentie of contactgegevens die nodig zijn om een advertentie te plaatsen.
